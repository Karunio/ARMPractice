#include "gpio.h"

void SystemClock_Config();
void Error_Handler();
void PinConfig();

uint32_t LedPins[] = {LED_0_Pin, LED_1_Pin, LED_2_Pin, LED_3_Pin};

int main()
{
    HAL_Init();
    SystemClock_Config();
    
    __HAL_RCC_GPIOH_CLK_ENABLE();
    __HAL_RCC_GPIOA_CLK_ENABLE();
    __HAL_RCC_GPIOC_CLK_ENABLE();
    
    PinConfig();
    
    while(1)
    {
        for(int i = 0; i < 4; i++)
        {
            HAL_GPIO_WritePin(GPIOC, LedPins[i], GPIO_PIN_SET);
            HAL_Delay(500);
            HAL_GPIO_WritePin(GPIOC, LedPins[i], GPIO_PIN_RESET);
        }
    }
}

